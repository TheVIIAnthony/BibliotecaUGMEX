    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        DefaultCategoryDataset dcds = new DefaultCategoryDataset();
        for (int i = 0; i < TablaConsRegistros.getRowCount(); i++) {
            int cantidadAlumnos = Integer.parseInt(TablaConsRegistros.getValueAt(i, 1).toString());
            String carrera = TablaConsRegistros.getValueAt(i, 0).toString();
            dcds.setValue(cantidadAlumnos, carrera, carrera);
            JFreeChart ch = ChartFactory.createBarChart3D("Frecuencia de ingresos por carreras", "Cantidad", "Alumnos", dcds, PlotOrientation.VERTICAL, true, true, false);
            ChartPanel cp = new ChartPanel(ch);
            panel.add(cp);
            cp.setBounds(0, 0, 500, 450);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

